public class Loops {
    public static void main(String[] args) {
        for (int i = 0; i < 2; i++) {
            System.out.println("hello world");
        }
        System.out.println("\n");

        int i = 0;
        while (i < 2) {
            System.out.println("hello world");
            i++;
        }
        System.out.println("\n");

        i = 0;
        do {
            System.out.println("Hello world");
            i++;
        } while (i <2);
    }

}